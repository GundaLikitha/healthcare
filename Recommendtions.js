import React, { useState } from "react";
import MealList from "./MealList";
/*import './Recommendation.css'*/
import '../../App.css';
function Recommendtions() {
  const [mealData, setMealData] = useState(null);
  const [calories, setCalories] = useState(2000);

  function getMealData() {
    fetch(
      /*`https://api.spoonacular.com/mealplanner/generate?apiKey=29cf5925317239c23f181a6a5159951f9d252b3d&timeFrame=day&targetCalories=${calories}`*/
      `https://api.spoonacular.com/mealplanner/generate?apiKey=cb1c464d94f142c08b156c5beddade8b&timeFrame=day&targetCalories=${calories}`
    )
      .then((response) => response.json())
      .then((data) => {
        setMealData(data);
      })
      .catch(() => {
        console.log("error");
      });
  }

  function handleChange(e) {
    setCalories(e.target.value);
  }

  return (
    <div className="Recommendtions">
      <section className="controls">
        <input
          type="number"
          placeholder="Calories (e.g. 2000)"
          onChange={handleChange}
        />
        <button onClick={getMealData}>Get Daily Meal Plan</button>
      </section>
      {mealData && <MealList mealData={mealData} />}
    </div>
  );
}
 export default Recommendtions;





 

 
